; ***************************************************************************************
;
;							Print ASCII string at HL
;
; ***************************************************************************************

PrintStringAtHL:
		ld 		a,(hl)
		cp 		$20		
		ret 	c
		call 	SYSPrintChar
		inc 	hl
		jr 		PrintStringAtHL

