; ***************************************************************************************
;
;							Print ASCIIZ string at HL
;
; ***************************************************************************************

PrintStringAtHL:
		ld 		a,(hl)
		or 		a
		ret 	z
		call 	SYSPrintChar
		inc 	hl
		jr 		PrintStringAtHL

