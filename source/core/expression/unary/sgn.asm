Unary_Sgn:	;; [sgn]
		call	EvaluateIntegerTerm 		; get value
		call 	Int32Zero 					; integer sgn() code
		jp 		z,UnaryInt32False
		exx
		bit 	7,h
		exx
		jp 		nz,UnaryInt32True
		ld 		a,1
		jp 		Return8BitConstant

