Unary_Not:		;; [not]
		call 	EvaluateIntegerTerm 		; only an integer
		call 	__UNSwap 					; not and swap twice.
		call 	__UNSwap		
		ret

__UNSwap:
		ld 		a,h
		cpl
		ld 		h,a
		ld 		a,l
		cpl 	
		ld 		l,a
		exx
		ret

