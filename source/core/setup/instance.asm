;				Switch to instance beginning at DE and ending at HL
;
; ***************************************************************************************

SetCurrentInstance:
		ld 		(LowMemory),de 				; set low and high memory
		ld 		(HighMemory),hl 
		ld 		(CodeAddress),de 			; program code is at the start.

		ret


