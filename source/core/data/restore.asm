Command_RESTORE: ;; [restore]
			ld 		hl,(CodeAddress)		; HL+3 is the first token.
			inc 	hl
			inc 	hl
			inc 	hl
			ld 		(ReadDataPointer),hl 	; scan from here.
			ld 		a,$FF 					; scan required first time out.
			ld 		(DataScanRequired),a
			ret 					

